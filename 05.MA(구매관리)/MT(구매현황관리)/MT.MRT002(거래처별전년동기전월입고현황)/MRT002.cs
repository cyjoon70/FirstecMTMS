        #region 조회조건 TextChanged
        //공장
        private void txtPlantCd_TextChanged(object sender, EventArgs e)
        {
            txtPlantNm.Value = SystemBase.Base.CodeName("PLANT_CD", "PLANT_NM", "B_PLANT_INFO", txtPlantCd.Text, " AND CO_CD = '" + SystemBase.Base.gstrCOMCD + "'");
        }       
        //프로젝트번호
        private void txtProjectNo_TextChanged(object sender, EventArgs e)
        {
            txtProjectNm.Value = SystemBase.Base.CodeName("PROJECT_NO", "PROJECT_NM", "S_SO_MASTER", txtProjectNo.Text, " AND CO_CD = '" + SystemBase.Base.gstrCOMCD + "'");

            if (txtProjectNm.Value == "")
                txtProjectSeq.Text = "";
        }
        #endregion

        #region SearchExec()
        protected override void SearchExec()
        {
            //조회조건 필수 체크
            if (SystemBase.Validation.GroupBox_SaveSearchValidation(groupBox1))
            {
                this.Cursor = Cursors.WaitCursor;

                try
                {
                    string strGbn = "";
                    if (rdoCfmMvmt.Checked == true) strGbn = "S1";
                    else strGbn = "S2";

                    string strQuery = "usp_MRT002 @pTYPE = '" + strGbn + "'";

                    strQuery += ", @pMVMT_DT = '" + dtpDt.Text + "-01'";
                    strQuery += ", @pSL_MVMT_DT = '" + dtpDt.Text + "-01'";
                    strQuery += ", @pPLANT_CD = '" + txtPlantCd.Text + "'";
                    strQuery += ", @pPROJECT_NO = '" + txtProjectNo.Text + "'";
                    strQuery += ", @pPROJECT_SEQ = '" + txtProjectSeq.Text + "'";
                    strQuery += ", @pCO_CD = '" + SystemBase.Base.gstrCOMCD + "' ";

                    UIForm.FPMake.grdCommSheet(fpSpread1, strQuery, G1Head1, G1Head2, G1Head3, G1Width, G1Align, G1Type, G1Color, G1Etc, G1HeadCnt, false, true, 0, 0, true);

                    if (fpSpread1.Sheets[0].RowCount > 0) Set_Section();
                }
                catch (Exception f)
                {
                    SystemBase.Loggers.Log(this.Name, f.ToString());
                    MessageBox.Show(SystemBase.Base.MessageRtn("B0002"), SystemBase.Base.MessageRtn("Z0002"), MessageBoxButtons.OK, MessageBoxIcon.Error); //데이터 조회중 오류가 발생하였습니다.
                }

                this.Cursor = Cursors.Default;
            }
        }
        #endregion

        #region 그리드 Head Title 재정의
        private void Set_Section()
        {
            string strDtY = Convert.ToDateTime(dtpDt.Value).AddYears(-1).ToString();//전년
            string strDtM = Convert.ToDateTime(dtpDt.Value).AddMonths(-1).ToString();//전월

            fpSpread1.Sheets[0].ColumnHeader.Cells[0, 3].Text = dtpDt.Text.Replace("-", "년") + "월";
            fpSpread1.Sheets[0].ColumnHeader.Cells[0, 4].Text = strDtY.Substring(0, 7).Replace("-", "년") + "월";
            fpSpread1.Sheets[0].ColumnHeader.Cells[0, 6].Text = strDtM.Substring(0, 7).Replace("-", "년") + "월";
        }
        #endregion

    }
